import styles from './Card.module.css'

import PropTypes from 'prop-types'
import { useNavigate } from 'react-router-dom'

////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * * A tile component to take the user to different projects.
 * 
 * @param title The project title.
 * @param image The tile image.
 * @param path  A location to the project.
 * 
 * TODO: This component is not done yet.
 */
////////////////////////////////////////////////////////////////////////////////////////////////////
function Card({ title='Enter Title', image='', path='/' })
{
  //////////////////////////////////////////////////////////////////////////////////////////////////
  // * Member variables
  //////////////////////////////////////////////////////////////////////////////////////////////////
  const navigate = useNavigate();
  
  //////////////////////////////////////////////////////////////////////////////////////////////////
  // * Handles when someone clicks a card.
  //////////////////////////////////////////////////////////////////////////////////////////////////
  const handleCardClick = () => {
    navigate(path);
  }
  
  return(
    <div className={ styles.card } onClick={ handleCardClick }>
      <img
        className={ styles.cardimage }
        src={ image }
      />
      <h2 className={ styles.title }>
        { title }
      </h2>
    </div>
  );
};

////////////////////////////////////////////////////////////////////////////////////////////////////
  // * Prop data types.
////////////////////////////////////////////////////////////////////////////////////////////////////
Card.propTypes = {
  image: PropTypes.string,
  path:  PropTypes.string,
  title: PropTypes.string,
};

export default Card;
